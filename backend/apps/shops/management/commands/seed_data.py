class Command(BaseCommand):
    help = 'Заполняет базу данных тестовыми данными'

    def handle(self, *args, **options):
        self.stdout.write('Создание таксопарков...')
        for data in shops_DATA:
            taxipark, created = TaxiPark.objects.get_or_create(
                name=data['name'],
                defaults=data,
            )
            if created:
                self.stdout.write(f'  ✓ Создан: {taxipark.name}')
            else:
                self.stdout.write(f'  — Уже существует: {taxipark.name}')

        self.stdout.write('Создание категорий блога...')
        for cat_data in BLOG_CATEGORIES:
            cat, created = Category.objects.get_or_create(**cat_data)
            if created:
                self.stdout.write(f'  ✓ Категория: {cat.name}')

        self.stdout.write('Создание статей блога...')
        for post_data in BLOG_POSTS:
            category_slug = post_data.pop('category_slug')
            try:
                category = Category.objects.get(slug=category_slug)
            except Category.DoesNotExist:
                category = None
            post, created = Post.objects.get_or_create(
                slug=post_data['slug'],
                defaults={**post_data, 'category': category},
            )
            if created:
                self.stdout.write(f'  ✓ Статья: {post.title}')
            post_data['category_slug'] = category_slug

        self.stdout.write(self.style.SUCCESS('\n✅ Данные успешно загружены!'))